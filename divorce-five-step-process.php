    <p>This step involves a review of the Texas Family Law Code and the Court in which your action is filed or may be filed.  
       Depending on your case and whether it is a divorce, modification, enforcement action, termination or adoption, etc., 
       we will review the law surrounding your case and its issues and discuss what is likely to happen given the law, the court and/or county in which your case is filed.  
       There are resources available to us in this step as well that we will review and consider when necessary.
    </p>
    
    <h3>Step 3: Financial</h3>
    <p>We strongly believe that our role as your attorney is to help preserve as many of your assets as possible.  
       This step requires your assistance to bring your documentation to us.  
       If your case is a divorce case, we will provide you with forms to aid in your recovery of documentation.  
       We know that for some, this step can be particularly daunting, but we are here to help.  
       Your job is just to gather what information you can and to your ability, you can aid in the compilation of data that we will need in order 
       to put together our spreadsheet for a division of your assets and debts.  
       If you do not have access to your documentation, this may be a situation where we will need the court's intervention.  
       If your case is a modification case or enforcement case, we will talk to you about the necessary documentation which is typically your income statements.  
       In this arena, are also tax liabilities and possibly complex business valuations.  
       We are blessed to have in our contacts, professionals that are revered in their industries that will assist us in evaluating both areas.  
    </p>
    
    <h3>Step 4:  Costs of Litigation</h3>
    <p>Our firm operates under an Evergreen Retainer which means that when you retain our firm, your funds are deposited into an IOLTA account.  
       These funds belong to you until we bill from your account.  
       When your retainer reaches a minimum and/or we anticipate costly litigation in the near future, we will ask you to replenish your retainer.  
       Several years ago at the request of our clients, we put together a Possible Litigation Fees Checklist.  
       This form provides a breakdown of your expenses in each area of the litigation process.  
       This step is so important that we bill 26 times a year and discuss your IOLTA account at most meeting and during most phone calls.  
       While we want to work to preserve your assets, we also know that you may also need to pull from your assets in order to pay your attorney.  
       We work very hard in this office.  
       Often, we are leaving our families at home to prepare your case for trial.  
       We deserve to be paid for the work that we do which is why we will have many discussions regarding the costs of your case.  
       While we believe we are in the job of empowering our clients and helping them rebuild their lives, we cannot do so at the expense of our own.  
       If at any point in the process you are unable to replenish your retainer to the requested amount, we may need to withdraw from representing you.  
       If this occurs, we will work with you as much as possible to leave you in the best possible place for moving your case forward.  
    </p>
    
    <h3>Step 5:  Parenting/Co-parenting Needs</h3>
    <p>We see so many cases involving children that just break our hearts.  
       We see parents needlessly fighting and putting their children in the middle.  
       The cost is enormously great.  
       When you come to see us and if your case involves children, we do several things to address severe conflict.  
       We first attempt to make sure you are working well in Step 1 above, then we try to minimize the tension 
       by setting in place some parameters either through the court's assistance or working in the collaborative law process.  
       You will be continually learning how to navigate the needs of your children which need most often to be put above your own.  
       If you cannot work with the other co-parent, we're going to find a way to help the relationship so long as you both are willing to work together.  
