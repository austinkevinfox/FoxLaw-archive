  	<h2>Possession and Access</h2> 
    <p>Possession and access of a child falls within the possession circle.   
       Texas law uses the term &quot;possession and access&quot; to describe a child's possession schedule with each parent and/or conservator.  
       The term &quot;visitation&quot; is not used in Texas.  
       We don't use the term &quot;visitation&quot; because, borrowing a phrase from a respected colleague: "You visit Disneyland - you don't visit your children."
       As discussed previously, Texas law presumes that it is in the child's best interest that both parties be appointed joint managing conservators (JMC) 
       with equally significant roles in the child's life; thereby avoiding any terms that may imply an inferiority of one conservator to the other.
    </p>
    <p>Did you know that every possession order must contain a provision that allows parties to do whatever they want to do with regard to possession of the children 
       and sharing time with the children so long as they both agree to what that is?  
       However, keep in mind that it's always best to get these agreements in writing (i.e. e-mail or text) 
       to prevent any miscommunication that may occur during a verbal exchange regarding possession.  
       It's only when the parties are not able to agree that the possession order found in the court's order prevails.  
       If this is the case, parties are required to refer to and follow the possession schedule as set out in their most current order 
       (i.e. temporary order, divorce decree, modification order, suit affecting parent-child relationship order, etc).  
    </p>
    <p>The <strong>Standard Possession Order</strong>, per Section 153 Subchapter F of the Texas Family Code, is presumed to be in the best interest of the child in all Texas family law cases, 
       unless one party produces evidence to the contrary such as abuse or endangerment to a child.  
       While the Standard Possession Order is the most widely used schedule by the Court, 
       it is important to note that it is the minimum amount of time that courts deem appropriate between a child and the non-primary parent. 
    </p>
    <p>A Standard Possession Order grants one parent (typically the non-primary parent) time with the child every Thursday evening during the normal school term from 6:00 p.m. to 8:00 p.m., 
       and every first, third, and fifth weekend of each month (as determined by the Fridays in each month) beginning at 6:00 p.m. on Friday and ending at 6:00 p.m. on the following Sunday. 
       In addition to these times, the non-primary parent is awarded 30 days in the summer months, a week during Christmas break, and alternating spring break and Thanksgiving holidays.
       The time to a parent under this schedule (counting overnights) is approximately 30%.
    </p>
    <p>See the Office of the Attorney General's 
       <a href="http://texasfamilylawyers.wordpress.com/2013/01/08/standard-possession-order-for-2013/" target="_blank">2013 Standard Possession Calendar.</a><br />
       <em>*Please note that this calendar does not take into account school holidays or extended summer possession periods.</em>
    </p> 
	<p>If a parent wishes to receive more time with the child, he or she may request an <strong>Expanded Standard Possession Order</strong> per Section 153.317 of the Texas Family code 
       which allows for a non-primary parent to make certain elections to extend the duration of the standard possession order.  
       For example, on Thursdays, the non-primary parent can choose to pick up the child when school is dismissed on Thursday and/or return the child when the child's school resumes 
       the following Friday and on weekends the non-primary parent can choose to pick up the child when school is dismissed on Friday and/or return the child when the child's school resumes 
       the following Monday.  
       The Expanded Standard Possession Order also lengthens the time available for holiday periods of possession 
       (Spring Break, Thanksgiving, Christmas, Mother's Day, Father's Day, etc), and for extended weekends due to holidays.  
       The benefit of an Expanded Standard Possession Order to many non-primary parents is that it creates a period of uninterrupted visitation for four nights, 
       lasting from the end of school Thursday through the following Monday morning for the non-primary parent's weekend possession. 
       This duration can prevent conflict that might otherwise occur during exchanges of the child, as well as foster the child's relationship with the non-primary parent.
       Please note that while having a schedule that may avoid conflict can be helpful, courts still expect parents to co-parent, be civil,
       and work together for the best interest of the child.
       It is said that divorce is difficult and made more difficult when parents fight, particularly at drop-offs and pick ups.
       The stress placed on children in these situations is immeasurable.
    </p> 
    <p>Per Section 153.255 of the Texas Family Code, parties that desire a possession schedule that deviates from the Standard Possession or Expanded Standard Possession Order 
       certainly have the flexibility to be as creative as they want or need to be when shaping a possession schedule that best benefits that family's needs.  
       In a litigated case; however, the Court will review the evidence and if the evidence shows that another arrangement would be in the child's best interest, 
       that the Standard Possession Order is just unworkable (i.e. due to the parties' work schedules), 
       that the child is less than 3 years old, or that there is a recent history of family violence, the Court may decide to deviate from the Standard Possession Order.
    </p>
    <p>Alternative possession schedules may include the following:
    </p>
    <ul>
		<li>Alternating Weeks, With or Without Weeknight During Other Parent's Week [50/50]</li>
		<li>2-2-5-5 Wrap Schedule [Mon-Tues every week, Wed-Thurs every week, Alternate Weekends]</li>
		<li>2-2-3/2-2-3 Possession Schedule [Mon-Tues every other week, Wed-Thurs every other week, Alternate Weekends]</li>
		<li>Under Three Years of Age </li>
		<li>Airline, Police Officer, or Firefighter's Possession Schedule</li>
		<li>Custom Possession Schedule [designed by parties / mix of the above]</li>
		<li>Supervised Possession</li>
    </ul>
