  	<h2>Conservatorship, Possession and Child Support</h2> 
    <p>I often find that the people who come into my office for a consultation have a skewed view or a difficult time understanding the differences between conservatorship, 
       possession, and child support under the Texas Family Code.  
       After years of trying to verbally explain the differences, I decided to create a <a href="images/conservatorship-possession-child-support.png" rel="prettyPhoto">visual aid</a> to help our prospective clients 
       better understand <a href="images/conservatorship-possession-child-support.png" rel="prettyPhoto">the differences between these three areas of law.</a>
       It is important for each party to clearly understand the role that each area plays in that person's life after a temporary or final order is entered with the Court.     
    </p>
        
    <h2>Conservatorship</h2>
    <p>I would like to point out that the Texas Family Code does not use the term &quot;custody&quot; in describing a parent's legal relationship with a child,
       but instead utilizes the more legalistic term &quot;conservatorship.&quot;
       This terminology focuses on the rights and duties of the parent-child relationship.
       People might inadvertently use the term &quot;custody&quot; in discussions since the term is so widely used in his/her circle of friends or in the press and media, 
       but the legal difference between &quot;custody&quot; and &quot;conservatorship&quot; should remain clear despite how people may misuse these terms in conversation.  
       There is no legal term such as &quot;primary custody,&quot; &quot;sole custody,&quot; &quot;joint custody,&quot; or any other type of &quot;custody&quot; over children under Texas law.  
       Instead, what follows is a discussion of the conservatorship law in Texas.
    </p>
    
    <h3>Managing and Possessory Conservatorship</h3>
    <p>There are two types of conservatorship in Texas: managing and possessory.</p>
    
    <p><strong>Managing conservators</strong> have the authority to &quot;manage&quot; the following decisions for the child independently, exclusively, 
        and/or subject to the agreement of the other parent conservator:
    </p>
    
    <ul>
    	<li>Primary residence of the child</li>
        <li>Medical, dental and surgical treatment involving invasive procedures</li>
        <li>Psychological and psychiatric treatment</li>
        <li>Education</li>
        <li>Receipt of child support funds for the benefit of the child</li>
        <li>Legal rights and representation</li>
        <li>Consent to marriage</li>
        <li>Consent to armed forces enlistment</li>
        <li>Services and earnings of the child (as a teenager)</li>
        <li>Estate of the child</li>
    </ul>
    
    <p><strong>Possessory conservators,</strong> on the other hand, essentially only enjoy the right of possession and access to the child under a defined possession schedule.
       A court must appoint a parent as either a managing or possessory conservator unless it finds that allowing possession and access would endanger the physical or 
       emotional welfare of the child and that it is not in the child's best interest to grant such possession or access.  
       Thus, in cases of abuse or endangerment, a court may require supervised possession or severely restrict or eliminate a parent's right to possession or access to a child.
    </p>
    
    <h3>Types of Managing Conservatorship</h3>
    <p>There are also two types of managing conservatorship: <strong>Joint Managing Conservatorship (hereinafter &quot;JMC&quot;),</strong> where two (or more) persons share the rights 
       and responsibilities of a managing conservatorship; however, the exclusive right to make certain decisions may be awarded to one party; 
       and <strong>Sole Managing Conservatorship (hereinafter &quot;SMC&quot;),</strong> where one person holds all managing conservatorship rights and duties listed above.
    </p>
    
    <p>Texas law presumes that both parties should be appointed JMC and contains limited grounds for rebutting that presumption.  
       In general, most interested parties who have not committed family violence or otherwise do not pose a risk of harm to the child will be appointed as a JMC.  
       However, JMC does not necessarily result in equal time with the child.  
       While that does happen in some cases, it is usually by agreement of the parties or based on a recommendation by a Guardian Ad Litem 
       which is ultimately adopted by the Court or agreed to between the parties.
    </p>
    
    <h3>The Exclusive Right to Designate the Primary Residence</h3>
	<p>The exclusive right to designate the primary residence of the child falls within the conservatorship circle.  
       A large majority of contested cases involve one or both party's focus upon obtaining the exclusive right to designate the primary residence of the child.  
       Unfortunately, parties often assume that by obtaining &quot;primary&quot; that he or she will receive more time with the child; 
       however, that is not always the case.  
       Possession is its own circle of law which I have discussed in further detail herein below.  
       The right to designate the primary residence of that child is simply just that and is typically restricted to a geographic area, unless otherwise agreed to by the parties.  
       One factor that the Court considers when appointing a parent as the &quot;primary&quot; parent is which parent is going to be the best at encouraging 
       the other parent's relationship with the child.
    </p>

	<h3>Geographic Restrictions</h3>
	<p>Texas Law provides for the ability to restrict the <em>Exclusive Right to Designate the Primary Residence</em> to within a certain geographic area: 
       for instance, a county within Texas, or even a particular school district. 
       A geographic restriction is based on the child's best interest, not the interest of one or both of the parties. 
       Therefore, in cases where both parties remain an active part of a child's life, it is not difficult to understand why a court would find that it is in the child's best interest 
       that the child's primary residence be restricted to a certain geographic area so that the parent who has the exclusive right to determine that residence does not move far 
       from the other parent and effectively deprive the child of contact with the non-primary parent.
    </p>
    <p>Geographic restrictions are quite common in most orders; however, parties may agree to waive them, and they often are conditioned on the non-primary parent remaining within that area.  
       If the non-primary parent moves away, then there is no reason to force the primary parent to abide by a restriction designed to foster a relationship with the non-primary parent 
       if said move is permanent. 
       The primary parent may also later petition the court to lift the restriction if circumstances change: 
       for example, if the non-primary parent does not maintain a strong relationship with the child.
    </p>

